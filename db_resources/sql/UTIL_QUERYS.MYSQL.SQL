SELECT U.ID_USUARIO,R.ROL,U.EMAIL_USUARIO
FROM   ROLES_USUARIOS RU,ROLES R,USUARIOS U
WHERE  1=1
AND    RU.EMAIL_USUARIO = U.EMAIL_USUARIO
AND    RU.ROL           = R.ROL;

SELECT R.ROL
FROM   ROLES_USUARIOS RU,ROLES R,USUARIOS U
WHERE  1=1
AND    RU.EMAIL_USUARIO = U.EMAIL_USUARIO
AND    RU.ROL           = R.ROL
AND    U.EMAIL_USUARIO  = 'alfredo.estrada@gonet.us';

SELECT U.ID_USUARIO,U.EMAIL_USUARIO,U.ID_PAIS_NATAL,U.ID_PAIS_RESIDENCIA
FROM   USUARIOS U;

SELECT U.ID_USUARIO,U.EMAIL_USUARIO,U.NOMBRES,U.APELLIDO_PATERNO,U.APELLIDO_MATERNO,P.CLAVE_ISO3166_2 AS CVE_PAIS,PR.CLAVE_ISO3166_2 AS CVE_PAIS_R,U.TELEFONO_MOVIL, U.FECHA_NACIMIENTO,U.GENERO
FROM   CAT_PAISES P,USUARIOS U LEFT JOIN CAT_PAISES PR ON U.ID_PAIS_RESIDENCIA = PR.ID_PAIS 
WHERE  1=1
AND    U.ID_PAIS_NATAL  = P.ID_PAIS;

SELECT ID_TIPO_EDUCACION,DESCRIPCION FROM CAT_TIPO_EDUCACION;

SELECT I.ID_INSTITUCION,P.CLAVE_ISO3166_2,I.NOMBRE,I.URL_PORTAL 
FROM   INSTITUCIONES_EDUCATIVAS I,CAT_PAISES P
